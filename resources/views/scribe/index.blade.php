        <h1 id="introduction">Introduction</h1>
<p>Dokumentasi lengkap API untuk Sistem Manajemen Rekrutmen. Kelola lowongan pekerjaan, lamaran kerja, dan peran pengguna dengan mudah.</p>
<aside>
    <strong>Base URL</strong>: <code>http://localhost:8000</code>
</aside>
<pre><code>Selamat datang di dokumentasi API Sistem Rekrutmen.

API ini memungkinkan Anda untuk mengelola lowongan pekerjaan, menangani lamaran kerja dengan upload CV, dan mengontrol akses pengguna dengan sistem berbasis peran.

**Fitur Utama:**
- üîê Autentikasi dengan Laravel Sanctum
- üë• Kontrol akses berbasis peran (Admin, HR, User)
- üìÑ Manajemen lowongan pekerjaan
- üìé Manajemen lamaran dengan upload file CV
- ‚úÖ Pelacakan status lamaran

&lt;aside&gt;Gunakan contoh kode di sebelah kanan untuk mengintegrasikan API dalam berbagai bahasa pemrograman.&lt;/aside&gt;</code></pre>

        <h1 id="authenticating-requests">Authenticating requests</h1>
<p>To authenticate requests, include an <strong><code>Authorization</code></strong> header with the value <strong><code>"Bearer {TOKEN_AUTENTIKASI_ANDA}"</code></strong>.</p>
<p>All authenticated endpoints are marked with a <code>requires authentication</code> badge in the documentation below.</p>
<p>Anda dapat mengambil token dengan login melalui endpoint <code>POST /api/login</code>. Token akan berlaku selama sesi Anda.</p>

